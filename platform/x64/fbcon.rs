//! Framebuffer console — renders text on the Bochs VGA framebuffer.
//!
//! Uses an embedded 8x16 bitmap font (CP437) to draw characters directly
//! into the linear framebuffer. Provides a Printer implementation so kernel
//! log output goes to the screen as well as serial.

use core::sync::atomic::{AtomicBool, AtomicU32, AtomicUsize, Ordering};
use crate::print::Printer;

// ─── Framebuffer state (set by bochs_fb driver) ─────────────────────────────

static FB_VADDR: AtomicUsize = AtomicUsize::new(0);
static FB_WIDTH: AtomicU32 = AtomicU32::new(0);
static FB_HEIGHT: AtomicU32 = AtomicU32::new(0);
static FB_STRIDE: AtomicU32 = AtomicU32::new(0); // bytes per row
static INITIALIZED: AtomicBool = AtomicBool::new(false);

// Cursor position (in character cells)
static CURSOR_X: AtomicU32 = AtomicU32::new(0);
static CURSOR_Y: AtomicU32 = AtomicU32::new(0);

const FONT_WIDTH: u32 = 8;
const FONT_HEIGHT: u32 = 16;

// Colors (BGRA8888)
const FG_COLOR: u32 = 0xFFCCCCCC; // Light gray text
const BG_COLOR: u32 = 0xFF1A1A2E; // Dark blue background

/// Initialize the framebuffer console with the given parameters.
/// Called after the bochs_fb driver has set up the framebuffer.
pub fn init(vaddr: usize, width: u32, height: u32, stride: u32) {
    FB_VADDR.store(vaddr, Ordering::Relaxed);
    FB_WIDTH.store(width, Ordering::Relaxed);
    FB_HEIGHT.store(height, Ordering::Relaxed);
    FB_STRIDE.store(stride, Ordering::Relaxed);
    CURSOR_X.store(0, Ordering::Relaxed);
    CURSOR_Y.store(0, Ordering::Relaxed);
    INITIALIZED.store(true, Ordering::Release);
}

pub fn is_initialized() -> bool {
    INITIALIZED.load(Ordering::Acquire)
}

fn cols() -> u32 {
    FB_WIDTH.load(Ordering::Relaxed) / FONT_WIDTH
}

fn rows() -> u32 {
    FB_HEIGHT.load(Ordering::Relaxed) / FONT_HEIGHT
}

/// Draw a single character at the given cell position.
#[allow(unsafe_code)]
fn draw_char(col: u32, row: u32, ch: u8) {
    let fb = FB_VADDR.load(Ordering::Relaxed);
    if fb == 0 { return; }

    let stride = FB_STRIDE.load(Ordering::Relaxed) as usize;
    let px = (col * FONT_WIDTH) as usize;
    let py = (row * FONT_HEIGHT) as usize;
    let glyph = &FONT_8X16[ch as usize];

    for gy in 0..FONT_HEIGHT as usize {
        let row_bits = glyph[gy];
        let row_base = fb + (py + gy) * stride + px * 4;
        for gx in 0..FONT_WIDTH as usize {
            let pixel = if row_bits & (0x80 >> gx) != 0 { FG_COLOR } else { BG_COLOR };
            unsafe {
                (row_base as *mut u32).add(gx).write_volatile(pixel);
            }
        }
    }
}

/// Scroll the screen up by one line. Copies all rows up and clears the last row.
#[allow(unsafe_code)]
fn scroll_up() {
    let fb = FB_VADDR.load(Ordering::Relaxed);
    if fb == 0 { return; }

    let stride = FB_STRIDE.load(Ordering::Relaxed) as usize;
    let width = FB_WIDTH.load(Ordering::Relaxed) as usize;
    let height = FB_HEIGHT.load(Ordering::Relaxed) as usize;
    let font_h = FONT_HEIGHT as usize;

    // Copy rows up by FONT_HEIGHT pixels
    let copy_bytes = stride * (height - font_h);
    unsafe {
        let dst = fb as *mut u8;
        let src = (fb + stride * font_h) as *const u8;
        core::ptr::copy(src, dst, copy_bytes);
    }

    // Clear the last row
    let last_row_start = fb + stride * (height - font_h);
    for y in 0..font_h {
        let row = last_row_start + y * stride;
        for x in 0..width {
            unsafe {
                (row as *mut u32).add(x).write_volatile(BG_COLOR);
            }
        }
    }
}

/// Write a single byte to the framebuffer console.
fn put_char(ch: u8) {
    if !is_initialized() { return; }

    let max_cols = cols();
    let max_rows = rows();
    if max_cols == 0 || max_rows == 0 { return; }

    let mut x = CURSOR_X.load(Ordering::Relaxed);
    let mut y = CURSOR_Y.load(Ordering::Relaxed);

    match ch {
        b'\n' => {
            x = 0;
            y += 1;
        }
        b'\r' => {
            x = 0;
        }
        b'\t' => {
            x = (x + 8) & !7;
        }
        8 | 0x7F => {
            // Backspace
            if x > 0 {
                x -= 1;
                draw_char(x, y, b' ');
            }
        }
        _ => {
            if ch >= 0x20 {
                draw_char(x, y, ch);
                x += 1;
            }
        }
    }

    // Wrap
    if x >= max_cols {
        x = 0;
        y += 1;
    }

    // Scroll if needed
    if y >= max_rows {
        scroll_up();
        y = max_rows - 1;
    }

    CURSOR_X.store(x, Ordering::Relaxed);
    CURSOR_Y.store(y, Ordering::Relaxed);
}

/// Printer implementation for the framebuffer console.
pub struct FbConPrinter;

impl Printer for FbConPrinter {
    fn print_bytes(&self, s: &[u8]) {
        for &ch in s {
            put_char(ch);
        }
    }
}

